SDL_Texture* font_texture = NULL;
int render_translate_x, render_translate_y;

void render_set_color(unsigned int rgba) {
    int r = (rgba >> 24) & 0xFF;
    int g = (rgba >> 16) & 0xFF;
    int b = (rgba >>  8) & 0xFF;
    int a = (rgba >>  0) & 0xFF;
    SDL_SetRenderDrawColor(renderer, r, g, b, a);
    if (font_texture) {
        SDL_SetTextureColorMod(font_texture, r, g, b);
        SDL_SetTextureAlphaMod(font_texture, a);
    }
}

void render_fill_auto(int* pos, int* len, int size) {
    if (*pos == AUTO && *len == AUTO) {
        *pos = 0;
        *len = size;
    }
    else if (*pos == AUTO) *pos = size - *len;
    else if (*len == AUTO) *len = size - *pos;
}

void render_get_size(int* w, int* h) {
    SDL_Texture* texture = SDL_GetRenderTarget(renderer);
    if (texture) SDL_QueryTexture(texture, NULL, NULL, w, h);
    else SDL_GetWindowSize(window, w, h);
}

void render_rect_fill(int x, int y, int w, int h) {
    int width, height;
    render_get_size(&width, &height);
    render_fill_auto(&x, &w, width);
    render_fill_auto(&y, &h, height);
    if (w < 0 || h < 0) return;
    SDL_Rect rect = (SDL_Rect){ x + render_translate_x, y + render_translate_y, w, h };
    SDL_RenderFillRect(renderer, &rect);
}

void render_rect_outl(int x, int y, int w, int h) {
    int width, height;
    render_get_size(&width, &height);
    render_fill_auto(&x, &w, width);
    render_fill_auto(&y, &h, height);
    if (w < 0 || h < 0) return;
    SDL_Rect rect = (SDL_Rect){ x + render_translate_x, y + render_translate_y, w, h };
    SDL_RenderDrawRect(renderer, &rect);
}

void render_circle(int x, int y, int r) {
    for (int X = 0; X < r * 2; X++) {
        for (int Y = 0; Y < r * 2; Y++) {
            int dx = r - X;
            int dy = r - Y;
            if (dx * dx + dy * dy < r * r) SDL_RenderDrawPoint(renderer, x + dx + render_translate_x, y + dy + render_translate_y);
        }
    }
}

void render_triangle(int x1, int y1, int x2, int y2, int x3, int y3) {
    unsigned char r, g, b, a;
    SDL_GetRenderDrawColor(renderer, &r, &g, &b, &a);
    SDL_Vertex vertices[] = {
        (SDL_Vertex){ { x1 + render_translate_x, y1 + render_translate_y }, { r, g, b, a }, { 0, 0 } },
        (SDL_Vertex){ { x2 + render_translate_x, y2 + render_translate_y }, { r, g, b, a }, { 0, 0 } },
        (SDL_Vertex){ { x3 + render_translate_x, y3 + render_translate_y }, { r, g, b, a }, { 0, 0 } },
    };
    SDL_RenderGeometry(renderer, NULL, vertices, 3, NULL, 3);
}

void render_line(int x1, int y1, int x2, int y2) {
    if (x1 == AUTO && x2 != AUTO) x1 = x2;
    if (x2 == AUTO && x1 != AUTO) x2 = x1;
    if (y1 == AUTO && y2 != AUTO) y1 = y2;
    if (y2 == AUTO && y1 != AUTO) y2 = y1;
    SDL_RenderDrawLine(renderer, x1 + render_translate_x, y1 + render_translate_y, x2 + render_translate_x, y2 + render_translate_y);
}

void render_scissor(int x, int y, int w, int h) {
    int width, height;
    render_get_size(&width, &height);
    render_fill_auto(&x, &w, width);
    render_fill_auto(&y, &h, height);
    SDL_Rect rect = (SDL_Rect){ x, y, w, h };
    SDL_RenderSetClipRect(renderer, &rect);
}

void render_unscissor() {
    SDL_RenderSetClipRect(renderer, NULL);
}

int text_size_internal(const char* string) {
    int max = 0;
    int cur = 0;
    int ptr = 0;
    char c;
    while ((c = string[ptr++]) != 0) {
        if (c == '\n') {
            if (cur > max) max = cur;
            cur = 0;
            continue;
        }
        cur++;
    }
    if (cur > max) max = cur;
    return max * 7;
}

int line_count(const char* string) {
    int count = 1;
    int ptr = 0;
    char c;
    while ((c = string[ptr++]) != 0) {
        if (c == '\n') count++;
    }
    return count;
}

void create_font_texture() {
    if (font_texture) return;
    uint32_t* data = malloc(98 * 98 * 4);
    for (int i = 0; i < 98 * 98; i++) {
        data[i] = ((font[i / 8] >> (7 - (i % 8))) & 1) ? 0xFFFFFFFF : 0x00000000;
    }
    SDL_Surface* surface = SDL_CreateRGBSurfaceWithFormatFrom(data, 98, 98, 32, 98 * 4, SDL_PIXELFORMAT_RGBA8888);
    SDL_Texture* texture = SDL_CreateTextureFromSurface(renderer, surface);
    SDL_FreeSurface(surface);
    free(data);
    font_texture = texture;
    uint8_t r, g, b, a;
    SDL_GetRenderDrawColor(renderer, &r, &g, &b, &a);
    SDL_SetTextureColorMod(font_texture, r, g, b);
    SDL_SetTextureAlphaMod(font_texture, a);
}

void render_text_internal(int x, int y, float ax, float ay, const char* string) {
    create_font_texture();
    x -= text_size_internal(string) * ax;
    y -= line_count(string) * ay;
    int ptr = 0;
    char c;
    int origx = x;
    while ((c = string[ptr++]) != 0) {
        if (c == '\n') {
            x = origx;
            y += 12;
        }
        if (c < 32 || c > 126) continue;
        SDL_Rect src = (SDL_Rect){ ((c - 32) % 14) * 7, ((c - 32) / 14) * 12 + 1, 7, 12 };
        SDL_Rect dst = (SDL_Rect){ x + render_translate_x, y + render_translate_y, 7, 12 };
        SDL_RenderCopy(renderer, font_texture, &src, &dst);
        x += 7;
    }
}

void render_text(int x, int y, const char* fmt, ...) {
    char buf[2048];
    va_list list;
    va_start(list, fmt);
    vsnprintf(buf, 2048, fmt, list);
    va_end(list);
    render_text_internal(x, y, 0, 0, buf);
}

void render_text_anchored(int x, int y, float ax, float ay, const char* fmt, ...) {
    char buf[2048];
    va_list list;
    va_start(list, fmt);
    vsnprintf(buf, 2048, fmt, list);
    va_end(list);
    render_text_internal(x, y, ax, ay, buf);
}

int text_size(const char* fmt, ...) {
    char buf[2048];
    va_list list;
    va_start(list, fmt);
    vsnprintf(buf, 2048, fmt, list);
    va_end(list);
    return text_size_internal(buf);
}